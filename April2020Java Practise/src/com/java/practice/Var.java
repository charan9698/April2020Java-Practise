package com.java.practice;

public class Var {
	public static void main(String[] args) {
		int a = (int) 5.6;// int has 4 bytes means 32 bits
		// int a = (int)5.6;is is know as type casting
		short b = 3560;// short is 2 bytes
		double c = 5.5;// double has 8 bytes
		float d = 5.5f;// float has 4 bytes
		String t = "balaji";
		char ch = 'b';// char has 2 bytes
		byte y = 19;// it has 1 byte
		boolean s = false;
		long l = 599688234;// it has 8 bytes
		System.out.println(l);
		System.out.println(s);
		System.out.println(y);
		System.out.println(c);
		System.out.println(d);
		System.out.println(b);
		System.out.println(a);
		System.out.println(t);
		System.out.println(ch);
	}

}
